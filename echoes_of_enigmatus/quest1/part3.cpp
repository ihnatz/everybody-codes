unsigned long long newEmi(long n, long exp, long mod) {
    unsigned long long current = n % mod;

    std::vector<long> rems;
    rems.reserve(8192);

    for (int i = 0; i < exp; ++i) {
        if (find(rems, current) != -1) {
            break;
        }
        rems.push_back(current);
        current = current * n % mod;
    }


    long cycleStart = find(rems, current);
    long cycleLen = rems.size() - cycleStart;
    long remaining = exp - cycleStart;
    long cycleCount = remaining / cycleLen;
    long leftover = remaining % cycleLen;

    long cycleSum = 0;
    for (size_t i = cycleStart; i < rems.size(); i++) cycleSum += rems[i];


    unsigned long long total = 0;
    for (int i = 0; i < cycleStart; ++i) total += rems[i];
    for (int i = cycleStart; i < cycleStart + leftover; ++i) total += rems[i];
    total += cycleSum * cycleCount;

    return total;
}


